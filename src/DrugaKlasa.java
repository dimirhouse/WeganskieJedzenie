public class DrugaKlasa {

    int x;
    double u;

    public DrugaKlasa(int x, double u) {
        this.x = x;
        this.u = u;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public double getU() {
        return u;
    }

    public void setU(double u) {
        this.u = u;
    }
}
